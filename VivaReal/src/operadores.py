#variáveis
i,j,x,y = 5, 7, 3.14, 2.75
#Somando
print("i + j = ", i + j)
print("x + y = ", x + y)
#Subtraindo
print("i - j = ", i - j)
print("x - y = ", x - y)
#Mutiplicando
print("i * j = ", i * j)
print("x * y = ", x * y)
#Dividindo
print("i / j = ", i / j)
print("x / y = ", x / y)
#Divisão inteira
print("I // j = ", i // j)
print("x // y = ", x // y)
#Módulo da divisão
print("i % j = ", i % j)
print("x % y = ", x % y)
#Potência
print("i^j =", i**j)
print("x^y =", x**y)
